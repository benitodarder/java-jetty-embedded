package local.tin.tests.utils.ftp.model;

/**
 *
 * @author benitodarder
 */
public class FTPUtilsException extends Exception {

    public FTPUtilsException() {
    }

    public FTPUtilsException(String message) {
        super(message);
    }

    public FTPUtilsException(String message, Throwable cause) {
        super(message, cause);
    }

    public FTPUtilsException(Throwable cause) {
        super(cause);
    }

    public FTPUtilsException(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
        super(message, cause, enableSuppression, writableStackTrace);
    }
    
    
    
}
